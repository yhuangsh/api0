-module(jsonres).

-export([success/3,
         failure/3]).

%%====================================================================
%% API
%%====================================================================

success(M, R, S) -> result(true, M, R, S).
failure(M, R, S) -> result(false, M, R, S).

%%====================================================================
%% Internal functions
%%====================================================================

result(TF, M, R, S) -> {jsx:encode(#{<<"success">> => TF, <<"message">> => M}), R, S}.
